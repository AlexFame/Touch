export function getInitialLang(tg) {
  const code = tg?.initDataUnsafe?.user?.language_code || "";
  return code.toLowerCase().startsWith("uk") ? "uk" : "ru";
}

export function apiLang(lang) {
  return lang === "uk" ? "ua" : "ru";
}
